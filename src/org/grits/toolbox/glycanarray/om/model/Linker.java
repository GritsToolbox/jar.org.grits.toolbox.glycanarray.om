package org.grits.toolbox.glycanarray.om.model;

public class Linker {
	
	String linkerName;
	String linkerSequence;
	Integer linkerId;
	String linkerURL;
	String linkerDescription;
	
	public String getLinkerName() {
		return linkerName;
	}
	public void setLinkerName(String linkerName) {
		this.linkerName = linkerName;
	}

	public String getLinkerSequence() {
		return linkerSequence;
	}
	public void setLinkerSequence(String linkerSequence) {
		this.linkerSequence = linkerSequence;
	}
	
	public Integer getLinkerId() {
		return linkerId;
	}
	public void setLinkerId(Integer linkerId) {
		this.linkerId = linkerId;
	}
	
	public String getLinkerURL() {
		return linkerURL;
	}
	public void setLinkerURL (String linkerURL) {
		this.linkerURL = linkerURL;
	}
	
	public String getLinkerDescription() {
		return linkerDescription;
	}
	public void setLinkerDescription (String linkerDescription) {
		this.linkerDescription = linkerDescription;
	}
	
	

}
